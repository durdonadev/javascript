// Part 1: Basic Arithmetic Operations
var num1 = 10;
var num2 = 20;
console.log(num1);
console.log(num2);

var addition = num1 + num2;
console.log(addition);
var substraction = num1 - num2;
console.log(substraction);
var multiply = num1 * num2;
console.log(multiply);
var division = num1 / num2;
console.log(division);

// Part 2: Temperature Converter

var fahrenheit1 = 350;
var celsius1 = (fahrenheit1 - 32) * (5 / 9);

console.log(fahrenheit1);
console.log(celsius1);

var celsius2 = 180;
var fahrenheit2 = celsius2 * (9 / 5) + 32;

console.log(celsius2);
console.log(fahrenheit2);

// Part 3: Interest Calculator

var principal = 10000;
var rate = 3.5;
var time = 5;
var interest = (principal * rate * time) / 100;
console.log(interest);
